package module_6;

public class Estruturas_de_Repeticao_Modulo_6 {

	public static void main(String[] args) {
		
		int i;
		
		for(i = 0 ; i <= 10; i++)
			System.out.println(i);
		
		i = 0;
		
		do {
			System.out.println(i);
			i++;
		}while(i <= 10);
		
		i = 0;
		
		while(i <= 10)
			System.out.println(i++);

	}

}
